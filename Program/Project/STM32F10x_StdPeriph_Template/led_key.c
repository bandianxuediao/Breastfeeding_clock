#include "led_key.h"
#define STM32F10X_LD_VL
////////////////////////////////////////////////////////////////////////////////////

const uint16_t LED_PIN[] = {GPIO_Pin_6, GPIO_Pin_1, GPIO_Pin_10, GPIO_Pin_11};
const uint16_t KEY_PIN[] = {GPIO_Pin_0, GPIO_Pin_1, GPIO_Pin_2,  GPIO_Pin_3};
////////////////////////////////////////////////////////////////////////////////////
void STM_LED_Init(Led_TypeDef LED) //初始化led
{
	GPIO_InitTypeDef  GPIO_InitStructure;
	GPIO_InitStructure.GPIO_Pin = LED_PIN[LED];
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_OD;
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;

	GPIO_Init(GPIOC, &GPIO_InitStructure);
}
////////////////////////////////////////////////////////////////////////////////////
void STM_KEY_Init(Key_TypeDef KEY)  //初始化按键
{
	GPIO_InitTypeDef  GPIO_InitStructure;
	GPIO_InitStructure.GPIO_Pin = KEY_PIN[KEY];
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IN_FLOATING;
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;

	GPIO_Init(GPIOE, &GPIO_InitStructure);
}
////////////////////////////////////////////////////////////////////////////////////
void NVIC0_Configuration(void)
{
	NVIC_InitTypeDef NVIC_InitStructure;
	NVIC_SetVectorTable(NVIC_VectTab_FLASH, 0x0);
	NVIC_PriorityGroupConfig(NVIC_PriorityGroup_1);
	NVIC_InitStructure.NVIC_IRQChannel = EXTI0_IRQn;
	NVIC_InitStructure.NVIC_IRQChannelPreemptionPriority = 0;
	NVIC_InitStructure.NVIC_IRQChannelSubPriority = 1;
	NVIC_InitStructure.NVIC_IRQChannelCmd = ENABLE;
	NVIC_Init(&NVIC_InitStructure);
}
